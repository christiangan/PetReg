public class Pet {
    String dateRegistered;
    String petHabitat;
    String species;
    String petName;
    String petColor;
    String breed;
    double weight;
    String dob;
    String petSex;
    String femaleClass;
    String tagClass;
    String tagNum;
    String contactOtherAnimals;
    String clientId;

    public String getDateRegistered() {
        return dateRegistered;
    }

    public void setDateRegistered(String dateRegistered) {
        this.dateRegistered = dateRegistered;
    }

    public String getPetHabitat() {
        return petHabitat;
    }

    public void setPetHabitat(String petHabitat) {
        this.petHabitat = petHabitat;
    }

    public String getSpecies() {
        return species;
    }

    public void setSpecies(String species) {
        this.species = species;
    }

    public String getPetName() {
        return petName;
    }

    public void setPetName(String petName) {
        this.petName = petName;
    }

    public String getPetColor() {
        return petColor;
    }

    public void setPetColor(String petColor) {
        this.petColor = petColor;
    }

    public String getBreed() {
        return breed;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getPetSex() {
        return petSex;
    }

    public void setPetSex(String petSex) {
        this.petSex = petSex;
    }

    public String getFemaleClass() {
        return femaleClass;
    }

    public void setFemaleClass(String femaleClass) {
        this.femaleClass = femaleClass;
    }

    public String getTagClass() {
        return tagClass;
    }

    public void setTagClass(String tagClass) {
        this.tagClass = tagClass;
    }

    public String getTagNum() {
        return tagNum;
    }

    public void setTagNum(String tagNum) {
        this.tagNum = tagNum;
    }

    public String getContactOtherAnimals() {
        return contactOtherAnimals;
    }

    public void setContactOtherAnimals(String contactOtherAnimals) {
        this.contactOtherAnimals = contactOtherAnimals;
    }

    public String getClientId() {
        return clientId;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }
}
